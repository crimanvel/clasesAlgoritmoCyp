#Dado dos números enteros, analizar si el 
# primero de los números ingresados es mayor que el segundo. Caso contrario, indicar que el primer número no es mayor que el segundo.
num1=int(input("Ingrese el primer número: "))
num2=int(input("Ingrese el segundo número: "))
if num1>num2 :
        print("El primer número ingresado:", num1,  " es mayor que el segundo número ingresado: ", num2)
else: 
        print("El primer número ingresado:", num1,  "NO es mayor que el segundo número ingresado: ", num2)
